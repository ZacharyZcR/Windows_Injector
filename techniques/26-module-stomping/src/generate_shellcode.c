void WriteShellcode(const char* filename, unsigned char* shellcode, size_t size) {
    HANDLE hFile = CreateFileA(filename, GENERIC_WRITE, 0, NULL, CREATE_ALWAYS, FILE_ATTRIBUTE_NORMAL, NULL);
    if (hFile == INVALID_HANDLE_VALUE) {
        printf("[-] Failed to create file %s (%u)\n", filename, GetLastError());
        return;
    }

    DWORD bytesWritten;
    if (!WriteFile(hFile, shellcode, (DWORD)size, &bytesWritten, NULL)) {
        printf("[-] Failed to write shellcode (%u)\n", GetLastError());
        CloseHandle(hFile);
        return;
    }

    CloseHandle(hFile);
    printf("[+] Generated %s (%zu bytes)\n", filename, size);
}

