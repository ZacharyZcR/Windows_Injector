void print_usage(const char *prog)
{
    printf("用法: %s <类型> [输出文件]\n\n", prog);
    printf("类型:\n");
    printf("  messagebox  - MessageBox shellcode\n");
    printf("  calc        - Calculator shellcode\n");
    printf("  exit        - Exit process shellcode\n");
    printf("\n示例:\n");
    printf("  %s messagebox payload.bin\n", prog);
    printf("  %s calc\n", prog);
}

int main(int argc, char *argv[])
{
    if (argc < 2) {
        print_usage(argv[0]);
        return 1;
    }

    const char *type = argv[1];
    const char *output = (argc >= 3) ? argv[2] : "payload.bin";

    unsigned char *shellcode = NULL;
    size_t size = 0;

    if (strcmp(type, "messagebox") == 0) {
        shellcode = messagebox_shellcode;
        size = sizeof(messagebox_shellcode) - 1;  // -1 去除末尾的 \0
    } else if (strcmp(type, "calc") == 0) {
        shellcode = calc_shellcode;
        size = sizeof(calc_shellcode) - 1;
    } else if (strcmp(type, "exit") == 0) {
        shellcode = exit_shellcode;
        size = sizeof(exit_shellcode) - 1;
    } else {
        printf("[!] 未知类型: %s\n\n", type);
        print_usage(argv[0]);
        return 1;
    }

    FILE *fp = fopen(output, "wb");
    if (!fp) {
        printf("[!] 无法创建文件: %s\n", output);
        return 1;
    }

    fwrite(shellcode, 1, size, fp);
    fclose(fp);

    printf("[+] 已生成 %s shellcode: %s (%lu 字节)\n", type, output, (unsigned long)size);

    return 0;
}
