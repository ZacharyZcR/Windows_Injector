int main(int argc, char* argv[]) {
    if (argc != 2) {
        printf("用法：%s <输出文件>\n", argv[0]);
        printf("示例：%s loop_payload.bin\n", argv[0]);
        return 1;
    }

    const char* outputFile = argv[1];

    // 使用死循环版本（更简单可靠）
    unsigned char* selected_shellcode = shellcode;
    unsigned int shellcodeSize = sizeof(shellcode);

    HANDLE hFile = CreateFileA(
        outputFile,
        GENERIC_WRITE,
        0,
        NULL,
        CREATE_ALWAYS,
        FILE_ATTRIBUTE_NORMAL,
        NULL
    );

    if (hFile == INVALID_HANDLE_VALUE) {
        printf("错误：无法创建文件（错误码：%u）\n", GetLastError());
        return 1;
    }

    DWORD bytesWritten = 0;
    if (!WriteFile(hFile, selected_shellcode, shellcodeSize, &bytesWritten, NULL)) {
        printf("错误：写入失败（错误码：%u）\n", GetLastError());
        CloseHandle(hFile);
        return 1;
    }

    CloseHandle(hFile);

    printf("✓ Shellcode 已生成：%s\n", outputFile);
    printf("  大小：%u 字节\n", bytesWritten);
    printf("  功能：无限循环（用于验证注入成功）\n");
    printf("\n");
    printf("测试方法：\n");
    printf("  1. 运行注入程序\n");
    printf("  2. 检查目标进程是否保持运行\n");
    printf("  3. 如果进程保持运行且 CPU 占用率高，说明注入成功\n");

    return 0;
}
